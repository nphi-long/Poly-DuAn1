    public void fillToTable() {
        try {
            tableModel.setRowCount(0);
            String sql = "Select * from NGUONTIEN where trangthai=''";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("tennguon"));
                v.add(rs.getString("loai"));
                tableModel.addRow(v);
            }
            tblHienThi.setModel(tableModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi " + e);
        }
    }

    public void showDetail() {
        int a = tblHienThi.getSelectedRow();

        txtTenNguon.setText(tblHienThi.getValueAt(a, 0).toString());
        if (tblHienThi.getValueAt(a, 1).toString().equals("Thu")) {
            rdoThu.setSelected(true);
        } else {
            rdoChi.setSelected(true);
        }
    }

    public boolean checkNull() {
        if (txtTenNguon.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống tên nguồn!");
            return false;
        } else if (!rdoThu.isSelected() && !rdoChi.isSelected()) {
            JOptionPane.showMessageDialog(this, "Không để trống loại!");
            return false;
        } else {
            return true;
        }
    }

