    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa?");
            if (choice == JOptionPane.YES_OPTION && choice >= 0) {
                String sql = "UPDATE NGUONTIEN SET trangthai='xoa' where tennguon=?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, txtTenNguon.getText());
                int m = ps.executeUpdate();
                if (m > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa dữ liệu thành công!");
                    fillToTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Không xóa được dữ liệu!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi xóa dữ liệu!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

