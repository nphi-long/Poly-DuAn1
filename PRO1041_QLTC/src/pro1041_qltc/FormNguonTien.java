    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkNull()) {
            try {
                String sql = "INSERT INTO NGUONTIEN(tennguon, loai) VALUES (?, ?)";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, txtTenNguon.getText());
                String loai;
                if (rdoThu.isSelected()) {
                    loai = rdoThu.getText();
                } else {
                    loai = rdoChi.getText();
                }
                ps.setString(2, loai);
                int m = ps.executeUpdate();
                if (m > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm dữ liệu thành công!");
                    fillToTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Không thêm được dữ liệu!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi lưu dữ liệu!");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

