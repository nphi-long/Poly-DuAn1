    public void LoadDataToCboHu() {
        try {
            String sql = "SELECT * FROM LUCHU ";
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                cboHu.addItem(rs.getString("tenhu"));
            }
            cboHu.removeItem("Tat ca");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load to combo hũ!");
        }
    }
    
